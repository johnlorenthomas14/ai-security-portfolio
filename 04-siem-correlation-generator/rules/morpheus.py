# Helpers
# ---------------------------------------------------------------------------


def _morpheus_condition(c: Condition) -> str:
    """Translate a canonical Condition into a cuDF-compatible boolean expr.

    Morpheus's filter stage accepts pandas/cuDF-style expressions evaluated
    on the deserialized PAYLOAD dataframe.
    """
    field = c.field
    op = c.operator.lower()
    val = c.value
    if op == "equals":
        return f'{field} == {_morpheus_value(val)}'
    if op == "in":
        if isinstance(val, (list, tuple)):
            quoted = ", ".join(_morpheus_value(v) for v in val)
            return f"{field}.isin([{quoted}])"
        return f"{field} == {_morpheus_value(val)}"
    if op == "gte":
        return f"{field} >= {val}"
    if op == "gt":
        return f"{field} > {val}"
    if op == "contains":
        # cuDF string contains — case-sensitive, returns boolean Series.
        return f'{field}.str.contains("{val}")'
    return f"{field} == {_morpheus_value(val)}"


def _morpheus_value(v) -> str:
    """Format a Python value as a cuDF-expr literal."""
    if isinstance(v, str):
        return f'"{v}"'
    if isinstance(v, bool):
        return "True" if v else "False"
    return str(v)


def _yaml_inline_list(items: list[str]) -> str:
    """Render a flat list as a YAML inline list — '[a, b, c]'."""
    if not items:
        return ""
    rendered = ", ".join(items)
    return f"[{rendered}]"


def _window_to_seconds(window: str) -> int:
    """Translate '1h', '30m', '15m', '24h', '1d', '7d' to seconds."""
    table = {
        "1h": 3600, "30m": 1800, "15m": 900, "5m": 300,
        "1d": 86400, "24h": 86400, "7d": 604800,
    }
    return table.get(window, 3600)
